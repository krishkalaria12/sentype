const typingText = document.querySelector(".typing-text p"),
inpField = document.querySelector(".wrapper .input-field"),
tryAgainBtn = document.querySelector(".content button"),
timeTag = document.querySelector(".time span b"),
mistakeTag = document.querySelector(".mistake span"),
wpmTag = document.querySelector(".wpm span"),
cpmTag = document.querySelector(".cpm span");

let timer,
maxTime = 30,  // Reduced test time to 30 seconds
timeLeft = maxTime,
charIndex = mistakes = isTyping = 0;

function loadParagraph() {
    const ranIndex = Math.floor(Math.random() * paragraphs.length);
    console.log("Random paragraph index: ", ranIndex);
    typingText.innerHTML = "";
    paragraphs[ranIndex].split("").forEach(char => {
        let span = `<span>${char}</span>`;
        typingText.innerHTML += span;
    });
    typingText.querySelectorAll("span")[0].classList.add("active");
    console.log("Paragraph loaded:", paragraphs[ranIndex]);

    document.addEventListener("keydown", () => {
        inpField.focus();
        console.log("Keydown event triggered, focusing input field.");
    });
    typingText.addEventListener("click", () => {
        inpField.focus();
        console.log("Typing text clicked, focusing input field.");
    });

    // Call the function to speak the first character
    speakCharacter(0);
}

function speakCharacter(index) {
    let characters = typingText.querySelectorAll("span");
    let char = characters[index].innerText;

    // Check if the character is a space or capital letter
    let utterance;
    if (char === " ") {
        utterance = new SpeechSynthesisUtterance("space");
        console.log("Saying: space");
    } else if (char === char.toUpperCase()) {
        utterance = new SpeechSynthesisUtterance("capital " + char);
        console.log("Saying: capital " + char);
    } else {
        utterance = new SpeechSynthesisUtterance(char);
        console.log("Saying: " + char);
    }

    // Increase the rate for faster speech
    utterance.rate = 2;  // Increased the speech rate for faster delivery
    window.speechSynthesis.speak(utterance);
}

function initTyping() {
    console.log("initTyping called, current charIndex:", charIndex, "timeLeft:", timeLeft);
    let characters = typingText.querySelectorAll("span");
    let typedChar = inpField.value.split("")[charIndex];
    console.log("Typed character: ", typedChar);

    if (charIndex < characters.length - 1 && timeLeft > 0) {
        if (!isTyping) {
            timer = setInterval(initTimer, 1000);
            isTyping = true;
            console.log("Timer started.");
        }

        if (typedChar == null) {
            console.log("No character typed, going back to previous character.");
            if (charIndex > 0) {
                charIndex--;
                if (characters[charIndex].classList.contains("incorrect")) {
                    mistakes--;
                }
                characters[charIndex].classList.remove("correct", "incorrect");
            }
        } else {
            if (characters[charIndex].innerText == typedChar) {
                characters[charIndex].classList.add("correct");
                console.log("Correct character typed:", typedChar);
            } else {
                mistakes++;
                characters[charIndex].classList.add("incorrect");
                console.log("Incorrect character typed:", typedChar);

                // Text-to-speech for mistakes
                let utterance = new SpeechSynthesisUtterance("wrong");
                console.log("Saying: wrong");
                utterance.rate = 2;  // Adjusted the rate for faster speech
                window.speechSynthesis.speak(utterance);
            }
            charIndex++;
        }

        characters.forEach(span => span.classList.remove("active"));
        characters[charIndex].classList.add("active");

        // Speak the next character after typing
        speakCharacter(charIndex);

        let wpm = Math.round(((charIndex - mistakes) / 5) / (maxTime - timeLeft) * 60);
        wpm = wpm < 0 || !wpm || wpm === Infinity ? 0 : wpm;

        console.log("Words per minute (WPM):", wpm);
        wpmTag.innerText = wpm;
        mistakeTag.innerText = mistakes;
        cpmTag.innerText = charIndex - mistakes;
    } else {
        clearInterval(timer);
        inpField.value = "";
        console.log("Time is up or all characters typed.");

        // Announce WPM and Mistakes when the test finishes
        let finalUtterance = new SpeechSynthesisUtterance(`Test complete. Your WPM is ${wpmTag.innerText} and you made ${mistakeTag.innerText} mistakes.`);
        finalUtterance.rate = 2;  // Faster speech for completion message
        window.speechSynthesis.speak(finalUtterance);
    }
}

function initTimer() {
    console.log("initTimer called, timeLeft:", timeLeft);
    if (timeLeft > 0) {
        timeLeft--;
        timeTag.innerText = timeLeft;
        let wpm = Math.round(((charIndex - mistakes) / 5) / (maxTime - timeLeft) * 60);
        wpmTag.innerText = wpm;
        console.log("Updated WPM:", wpm);
    } else {
        clearInterval(timer);
        console.log("Timer stopped, time is up.");
    }
}

function resetGame() {
    console.log("Resetting game...");
    loadParagraph();
    clearInterval(timer);
    timeLeft = maxTime;
    charIndex = mistakes = isTyping = 0;
    inpField.value = "";
    timeTag.innerText = timeLeft;
    wpmTag.innerText = 0;
    mistakeTag.innerText = 0;
    cpmTag.innerText = 0;
    console.log("Game reset, initial values set.");
}

// Start the test with a pre-test message
function startTest() {
    let preTestUtterance = new SpeechSynthesisUtterance("Test is beginning.");
    preTestUtterance.rate = 2;  // Faster speech rate
    window.speechSynthesis.speak(preTestUtterance);

    setTimeout(loadParagraph, 3000);  // Wait for 3 seconds before starting the test
}

startTest();

inpField.addEventListener("input", () => {
    console.log("Input event triggered.");
    initTyping();
});
tryAgainBtn.addEventListener("click", () => {
    console.log("Try Again button clicked.");
    resetGame();
});
